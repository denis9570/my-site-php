		<p align="left"><strong>Столетняя война</strong>  -
    наименование длительного военного
    конфликта между Англией и Францией
    (1337-1453), вызванного стремлением Англии
    вернуть принадлежавшие ей на континенте
    Нормандию, Мен, Анжу и др., а также
    династическими притязаниями английских
    королей на французский престол. Англия
    потерпела поражение, на континенте у нее
    сохранилось всего одно владение - порт
    Кале, удерживаемый до 1559 г.</p>
      <hr>Столетняя война 1337—1453 гг., война между Англией и Францией. Осн. 
      причины войны: стремление Франции вытеснить Англию с юго-запада страны 
      (провинция Гиень) и ликвидировать этот последний оплот английской власти на 
      франц. тер., а Англии — упрочиться в Гиени и вернуть утраченные ранее 
      Нормандию, Мен, Анжу и др. франц. области. Англо-французские противоречия 
      осложнялись соперничеством из-за Фландрии, формально находившейся под 
      властью франц. короля, но фактич. независимой и связанной торг, узами с 
      Англией (англ, шерсть была основой сукноделия во Фландрии). Поводом к 
      войне послужили притязания англ, короля <b>
    <a href="http://ru.wikipedia.org/wiki/%D0%AD%D0%B4%D1%83%D0%B0%D1%80%D0%B4_III" target="_blank">Эдуарда III</a></b> на французский престол. На 
      стороне Англии выступили герм, феодалы и Фландрия. Франция заручилась 
      поддержкой Шотландии и рим. папы. Англ, армия в основном была наёмной, 
      находилась под командованием короля. Её основу составляли пехота (лучники) 
      и наёмные рыцарские отряды. Основой франц. армии было феод, рыцарское 
      ополчение (см. Рыцарское войско).<p><b>Первый период</b> С. в. (1337-1360) 
      характеризовался борьбой сторон за Фландрию и Гиень. В Слёйсском морском 
      сражении 1340 англичане нанесли франц. флоту тяжёлое поражение и завоевали 
      господство на море. В авг. 1346 в битве при Креси они добились 
      превосходства на суше, а в ходе 11-мес. Осады овладели мор. крепостью и 
      портом Кале (1347). После почти 10-летнего перемирия (1347—55) англ, армия 
      развернула успешное наступление по овладению юго-западом Франции (Гиень и 
      Гасконь). В сражении при Пуатъе (1356) франц. армия вновь была 
      разгромлена. Непомерные налоги и поборы, установленные англичанами, и 
      царившая в стране разруха стали причиной восстаний франц. народа — 
      Парижское восстание под руководством Этьена Марселя 1357—58 и Жакерия 
      (1358). Это вынудило Францию подписать в Бретиньи (1360) мирный договор на 
      чрезвычайно тяжёлых условиях — передача Англии земель к югу от Луары до 
      Пиренеев.</p>
      <p><b>Второй период</b> С. в. (136 9—8 0). Стремясь ликвидировать завоевания 
      Англии, король Франции Карл V (правил в 1364—80) реорганизовал армию и 
      упорядочил налоговую систему. Франц. рыцарское ополчение было частично 
      заменено наёмными пех. отрядами, созданы полевая арт-я и новый флот. 
      Главнокоманд. армией (коннетаблем) был назначен талантливый военачальник 
      Б. Дгогск-лен, получивший широкие полномочия. Используя тактику внезапных 
      нападений и пар-тиз. войны, франц. армия к концу 70-х гг. постепенно 
      оттеснила англ, войска к морю. Успеху воен. действий способствовало 
      применение франц. армией арт-и. Сохранив ряд портов на побережье Франции 
      (Бордо, Байонна, Брест, Шербур, Кале) и часть франц. тер. между Бордо и 
      Байонной, Англия в связи с обострившейся обстановкой внутри страны (см. 
      Уота Тайлера восстание 1381) заключила перемирие с Францией, в к-рой также 
      начались нар. волнения.</p>
      <p><b>Третий период</b> С. в. (141 5—2 4). Воспользовавшись ослаблением Франции, 
      вызванным обострением внутр. противоречий (междоусобная война феод, 
      группировок — бургундцев и арманьяков, новые восстания крестьян и 
      горожан), Англия возобновила войну. В 1415 в сражении при Азенкуре 
      англичане нанесли поражение французам, с помощью вступившего с ними в союз 
      герцога Бургундского овладели Сев. Францией, что вынудило Францию 
      подписать 21 мая 1420 в Труа унизительный мирный договор. По условиям 
      договора Франция становилась частью объединённого англо-франц. 
      королевства. Англ, король Генрих V объявлялся правителем Франции на правах 
      регента, а после смерти франц. короля Карла VI получал права на франц. 
      престол. Однако в 1422 внезапно скончались и Карл VI и Генрих V. В 
      результате обострившейся борьбы за королевский престол (1422—23) Франция 
      оказалась в трагическом положении: расчленённой, разграбленной 
      захватчиками. Население на тер., занятой англичанами, было задавлено 
      налогами и контрибуцией. Поэтому для Франции война за королевский 
      престол переросла в национально-освободит. войну.</p>
