      <p><b>Четвёртый период</b> С. в. (1424-1453). С вступлением нар. масс в 
      войну нар-тиз. борьба (особенно в Нормандии) приобрела широкий размах. 
      Партиз. отряды оказывали большую помощь франц. армии: устраивали засады, 
      захватывали сборщиков налогов и уничтожали небольшие отряды пр-ка, 
      вынуждая англичан держать значит, гарнизоны в тылу завоёванной тер. Когда 
      в окт. 1428 англ, армия и бургундцы осадили Орлеан — последнюю сильную 
      крепость на не занятой французами тер., национально-освободит. борьба ещё 
      более усилилась. Её возглавила 
      <a href="http://ru.wikipedia.org/wiki/%D0%96%D0%B0%D0%BD%D0%BD%D0%B0_%D0%B4%E2%80%99%D0%90%D1%80%D0%BA" target="_blank"><strong>Жанна д'Арк</strong></a>, под рук-вом к-рой сражение за 
      Орлеан было выиграно (май 1429). В 1437 франц. войска взяли Париж, в 
      1441 — отвоевали Шампань, в 1459 — Мен и Нормандию, в 1453 — Гиень. 19 окт. 
      1453 английская армия капитулировала в Бордо. Это означало окончание войны.</p>
