      <p>С. в. принесла огромные бедствия франц. народу, нанесла большой урон 
      экономике страны, но она способствовала росту нац. самосознания. После 
      изгнания англичан завершился исторически прогрес. процесс объединения 
      Франции. В Англии С. в. временно закрепила господство феод, аристократии и 
      рыцарства, что замедлило процесс централизации гос-ва. С. в. 
      продемонстрировала преимущество англ, наёмного войска перед франц. феод, 
      рыцарским ополчением, что заставило Францию создать постоянное наёмное 
      войско. Это войско, находящееся на службе у короля, имело черты регулярной 
      армии в организации, воинской дисциплине, обучении (см. Ордонан-савые 
      роты). Политич. и материальной основой наёмного войска был союз 
      королевской власти и горожан, заинтересованных в преодолении феод, 
      раздробленности. Война показала, что тяжёлая рыцарская конница утратила 
      прежнее значение, увеличилась роль пехоты, особенно лучников, к-рые 
      успешно боролись с рыцарями. Появившееся в ходе войны огнестр. оружие хотя 
      и уступало луку и арбалету, но всё шире применялось в ходе сражений. 
      Изменение характера войны, превращение её в народную, освободительную 
      привело к освобождению Франции от захватчиков. 
